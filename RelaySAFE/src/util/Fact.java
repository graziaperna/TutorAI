package util;

public class Fact {
	private String fact;
	private double prob;

	public Fact(String f, double p) {
		fact = f;
		prob = p;
	}
	//ste prevedere un costruttore che invece che un fatto come stringa prenda nome predicato e lista argomenti?

	public String getFact() {
		return fact;
	}	
	public double getProb() {
		return prob;
	}
	public void setProb(double prob) {
		this.prob = prob;
	}

}
